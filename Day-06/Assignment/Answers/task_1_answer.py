# ## Task 1: Arithmetic Operators

# 1. Create two variables `a` and `b` with numeric values.
# 2. Calculate the sum, difference, product, and quotient of `a` and `b`.
# 3. Print the results.


a = 10
b = 20

def add(a , b):
    add = a + b
    return add

def sub(a , b):
    sub = a - b
    return sub

def multi(a , b):
    multi = a * b
    return multi

def div(a , b):
    div = a / b
    return div

print(add(a , b))
print(sub(a , b))
print(multi(a , b))
print(div(a , b))