# ## Task 3: Logical Operators

# 1. Create two boolean variables, `x` and `y`.
# 2. Use logical operators (`and`, `or`, `not`) to perform various logical operations on `x` and `y`.
# 3. Print the results.


x = True
y = False

and_operator = x and y
or_operator = x or y
not_operator = not x
not_operator_again = not y

print(f"AND Operator for {x} and {y} is {and_operator}")
print(f"OR Operator for {x} or {y} is {or_operator}")
print(f"NOT operator for {x} not {y} is {not_operator}")
print(f"NOT operator for {x} not {y} is {not_operator_again}")