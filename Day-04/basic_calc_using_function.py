def addition(num1 , num2):
    add = num1 + num2
    return add

def substraction(num1 , num2):
    sub = num1 - num2
    return sub

def multiply(num1 , num2):
    mul = num1 * num2
    return mul

def division(num1 , num2):
    div = num1 // num2
    return div


