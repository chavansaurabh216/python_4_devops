# Float variable declaration

number1 = 10
number2 = -20
number3 = abs(number2)

# Division
# using / --> Gives the floating result 

div = number3 / number1
print(div)

# using // --> Gives the integer result 

div_int = number3 // number1
print(div_int)


# Modulus --> Gives the remainder

modulus = number3 % number1
print(modulus)

# Absolute value --> gives the positive value for an integer

print(number3)